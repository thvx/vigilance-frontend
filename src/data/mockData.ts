export function generateRandomAlert(): Alert {
  const crimeTypes: CrimeType[] = ['robbery', 'assault', 'vandalism', 'theft', 'suspicious_activity', 'intrusion', 'vehicle_theft', 'armed_threat'];
  const onlineCameras = mockCameras.filter(c => c.status === 'online');
  const camera = onlineCameras[Math.floor(Math.random() * onlineCameras.length)];
  const crimeType = crimeTypes[Math.floor(Math.random() * crimeTypes.length)];
  const confidence = 0.7 + Math.random() * 0.28;
  const pa_triggered = confidence >= 0.85;
  
  return {
    id: `ALT-${Date.now()}`,
    camera_id: camera.id,
    camera_name: camera.name,
    timestamp: new Date().toISOString(),
    crime_type: crimeType,
    confidence,
    validation_status: 'pending',
    location: camera.location,
    pa_triggered,
    related_cameras: [],
    severity: confidence >= 0.9 ? 'critical' : confidence >= 0.85 ? 'high' : confidence >= 0.75 ? 'medium' : 'low',
  };
}

// Función para simular continuación de evento en otra cámara
export function generateRelatedAlert(originalAlert: Alert, cameras: Camera[]): Alert | null {
  const sourceCamera = cameras.find(c => c.id === originalAlert.camera_id);
  if (!sourceCamera) return null;

  // Buscar cámaras cercanas (simulación - en realidad usaría coordenadas)
  const nearbyIndexes = [
    (cameras.indexOf(sourceCamera) - 1 + cameras.length) % cameras.length,
    (cameras.indexOf(sourceCamera) + 1) % cameras.length,
  ];
  const nearbyCameras = nearbyIndexes.map(i => cameras[i]).filter(c => c.status === 'online');
  
  if (nearbyCameras.length === 0) return null;

  const nextCamera = nearbyCameras[Math.floor(Math.random() * nearbyCameras.length)];
  const confidence = Math.max(0.7, originalAlert.confidence - 0.05 + Math.random() * 0.1);

  return {
    id: `ALT-${Date.now()}`,
    camera_id: nextCamera.id,
    camera_name: nextCamera.name,
    timestamp: new Date().toISOString(),
    crime_type: originalAlert.crime_type,
    confidence,
    validation_status: 'pending',
    location: nextCamera.location,
    pa_triggered: confidence >= 0.85,
    related_cameras: [originalAlert.camera_id],
    severity: originalAlert.severity,
  };
}